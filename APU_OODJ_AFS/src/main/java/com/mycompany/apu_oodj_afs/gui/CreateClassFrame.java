    private void btnCreateClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateClassActionPerformed
        // TODO add your handling code here:
    // 1. Get text from all fields
    String classID = txtClassID.getText().trim();
    String className = txtClassName.getText().trim();
    String moduleCode = txtModuleCode.getText().trim();
    String semester = txtSemester.getText().trim();
    String maxCapacityStr = txtMaxCapacity.getText().trim();
    
    // 2. Validate that none are empty
    if (classID.isEmpty() || className.isEmpty() || moduleCode.isEmpty() || 
        semester.isEmpty() || maxCapacityStr.isEmpty()) {
        lblFeedback.setText("Error: All fields are required!");
        return;
    }
    
    // 3. Validate that Max Capacity is a valid Integer
    int maxCapacity;
    try {
        maxCapacity = Integer.parseInt(maxCapacityStr);
        if (maxCapacity <= 0) {
            lblFeedback.setText("Error: Max Capacity must be a positive number!");
            return;
        }
    } catch (NumberFormatException e) {
        lblFeedback.setText("Error: Max Capacity must be a valid integer!");
        return;
    }
    
    // 4. Create formatted string
    String classData = String.format("%s|%s|%s|%s|%d", 
                                      classID, className, moduleCode, semester, maxCapacity);
    
    // 5. Save to file
    if (saveClassToFile(classData)) {
        // 6. Show success message and clear fields
        lblFeedback.setText("Class " + classID + " Created");
        clearFields();
    } else {
        lblFeedback.setText("Error: Failed to save class to file!");
    }  
    }//GEN-LAST:event_btnCreateClassActionPerformed

