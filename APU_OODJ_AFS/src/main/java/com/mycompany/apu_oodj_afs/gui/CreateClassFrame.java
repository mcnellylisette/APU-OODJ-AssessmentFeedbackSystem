/**
 * Saves class data to the classes.txt file
 * @param classData The formatted class data string
 * @return true if successful, false otherwise
 */
private boolean saveClassToFile(String classData) {
    BufferedWriter writer = null;
    try {
        // Create data directory if it doesn't exist
        File dataDir = new File("data");
        if (!dataDir.exists()) {
            dataDir.mkdir();
        }
        
        // Append to classes.txt
        writer = new BufferedWriter(new FileWriter("data/classes.txt", true));
        writer.write(classData);
        writer.newLine();
        return true;
        
    } catch (IOException e) {
        logger.log(java.util.logging.Level.SEVERE, "Error writing to file", e);
        return false;
        
    } finally {
        // Ensure resources are closed
        if (writer != null) {
            try {
                writer.close();
            } catch (IOException e) {
                logger.log(java.util.logging.Level.SEVERE, "Error closing writer", e);
            }
        }
    }
}

